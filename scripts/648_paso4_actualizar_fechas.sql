UPDATE contadores_conceptos cc
SET
  lectura_inicial = fc.acs_lectura,
  fecha_lectura_inicial = fc.acs_fecha,
  lectura_actual = fc.acs_lectura,
  fecha_lectura_actual = fc.acs_fecha,
  updated_at = now()
FROM fechas_correctas fc
JOIN contadores c ON c.numero_serie = fc.numero_serie
JOIN conceptos con ON con.codigo = 'ACS'
WHERE cc.contador_id = c.id
  AND cc.concepto_id = con.id;

